
package calculator;

public class Calculator {
public int add(int a,int b){
    int sum;
    sum=a+b;
    return sum;
}
public float sub(int a,int b){
    int sub;
    sub=a-b;
    return sub;
}
public float mult(float a,float b){
    float mult;
    mult=a*b;
    return mult;
}
public float div(float a,float b){
    float div;
    div=a/b;
    return div;
}
}